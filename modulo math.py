import math #importa o modulo "math"

calcula_raiz = math.sqrt(81) #chama a funcao "sqrt" para calcular raiz quadrada do valor entre parenteses (parametro).

print(f"O valor da raiz quadrada é {calcula_raiz}")


#abaixo codigo solicitando um valor do usuario para calculo da raiz...

raiz2 = math.sqrt(eval(input("Digite o numero para calculo da raiz: ")))

print(f"O valor da raiz quadrada do numero escolhido é: {raiz2}")

